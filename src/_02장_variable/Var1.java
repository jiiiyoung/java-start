package _02장_variable;

public class Var1 {
    public static void main(String[] args){
        System.out.println(10);
        System.out.println(10);
        System.out.println(10);

        System.out.println(20); // 10 -> 20
        System.out.println(20); // 10 -> 20
        System.out.println(20); // 10 -> 20
        // 10을 출력하던걸 20으로 바꾸려면 3번의 코드를 변경해야한다.

    }
}
